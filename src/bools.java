public class bools {
    public static void main(String[] args){
        // yes or no ... on / off .... TRUE\FALSE

        boolean isJavaFun = true;
        boolean isSushiGreat = true;
        boolean isCatFishTasty = false;
        System.out.println(isJavaFun);     // Outputs true
        System.out.println(isSushiGreat);     // true
        System.out.println(isCatFishTasty);     // Outputs false

        int x = 10;
        int y = 9;
        System.out.println(x > y); // returns true, because 10 is higher than 9

        System.out.println(10 > 9); // returns true, because 10 is higher than 9

        System.out.println(10 == 15); // returns false, because 10 is not equal to 15

        int z = 10;
        System.out.println(z == 10); // returns true, because the value of x is equal to 10




    }
}
